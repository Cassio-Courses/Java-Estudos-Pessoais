package repetiçãofor;
public class RepetiçãoFor {
    
    public static void main(String[] args) {
        int cc;
        for ( cc = 0; cc <= 4; cc++){
            System.out.println(cc);
        }
        for (cc = 0; cc <= 100; cc += 10){
            System.out.println(cc);
            
        }
        System.out.println(cc);
    }
    
}

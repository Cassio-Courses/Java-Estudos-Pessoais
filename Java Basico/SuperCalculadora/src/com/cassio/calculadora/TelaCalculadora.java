    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        float num = Float.parseFloat(spinNumero.getValue().toString());
        lblRestDivisao.setText(Float.toString(num % 2));
        lblRaizCubica.setText(String.format("%.2f", Math.cbrt(num)));
        lblElevadoCubo.setText(Float.toString((float) Math.pow(num, 3)));
        lblValorAbsoluto.setText(Float.toString((float) Math.abs(num)));
        lblraizQuadrada.setText(String.format("%.2f",Math.sqrt(num)));
        lblNumero.setText(String.format("%.2f", num));
    }//GEN-LAST:event_btnCalcularActionPerformed

